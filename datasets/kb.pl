%% piatto_grasso(piatto con alta grassezza o untuosita)
piatto_grasso(Nome) :-
    piatto(Nome, _, _, Grass, Untu, _, _, _, _, _, _, _),
    (Grass >= 7 ; Untu >= 7).
 
%% piatto_succulento
piatto_succulento(Nome) :-
    piatto(Nome, _, _, _, _, Succ, _, _, _, _, _, _), Succ >= 7.
 
%% piatto_speziato
piatto_speziato(Nome) :-
    piatto(Nome, _, _, _, _, _, Spez, _, _, _, _, _), Spez >= 6.
 
%% piatto_dolce(piatto con tendenza dolce o dolcezza significativa)
piatto_dolce(Nome) :-
    piatto(Nome, _, TDolce, _, _, _, _, _, _, _, Dolc, _),
    (TDolce >= 6 ; Dolc >= 6).
 
%% piatto_sapido
piatto_sapido(Nome) :-
    piatto(Nome, _, _, _, _, _, _, Sap, _, _, _, _), Sap >= 7.
 
%% piatto_amaro
piatto_amaro(Nome) :-
    piatto(Nome, _, _, _, _, _, _, _, TAm, _, _, _), TAm >= 6.
 
%% piatto_acido
piatto_acido(Nome) :-
    piatto(Nome, _, _, _, _, _, _, _, _, TAc, _, _), TAc >= 6.
 
%% vino_fresco(vino ad alta acidita)
vino_fresco(Nome) :-
    vino(Nome, _, _, _, Acid, _, _, _, _, _, _), Acid >= 7.
 
%% vino_strutturato (vino con alta persistenza e alcolicita)
vino_strutturato(Nome) :-
    vino(Nome, _, _, _, _, _, Alc, _, _, _, Pers),
    Alc >= 7, Pers >= 7.
 
%% vino_tannico
vino_tannico(Nome) :- vino(Nome, _, _, _, _, _, _, Tann, _, _, _), Tann >= 7.
 
%% vino_morbido
vino_morbido(Nome) :-
    vino(Nome, _, _, _, _, _, _, _, Morb, _, _), Morb >= 7.
 
